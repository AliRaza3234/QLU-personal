def calculate_past_salary(target_year, country, current_salary):
    data = inflation
    matched_country, score = process.extractOne(country, data.keys())
    if score >= 80:
        country = matched_country
    else:
        return None
    if str(target_year) not in data[country]:
        year = int(target_year)
        closest_year = None
        for i in range(11):
            if (str(year + i)) in data[country]:
                closest_year = str(year + i)
                break
        if closest_year == None:
            for i in range(11):
                if (str(year - i)) in data[country]:
                    closest_year = str(year - i)
                    break
        return calculate_past_salary(int(closest_year), country, current_salary)

    adjusted_salary = current_salary
    inflation_rates = [
        (int(year), rate) for year, rate in data[country].items() if year.isdigit()
    ]
    inflation_rates.sort(reverse=True)
    for year, inflation_rate in inflation_rates:
        if year >= target_year:
            adjusted_salary /= 1 + (inflation_rate / 100)
        else:
            break
    return adjusted_salary
